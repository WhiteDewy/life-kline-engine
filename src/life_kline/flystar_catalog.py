SOURCE_FLIGHT_OVERRIDES: SourceFlightOverrides = {}





def _extract_target_house_meta(sections: TargetHouseSections) -> Dict[int, TargetHouseMeta]:
    return {
        house: {field: section[field] for field in FLIGHT_SECTION_META_FIELDS}
        for house, section in sections.items()
    }


def _extract_target_house_entries(sections: TargetHouseSections) -> TargetHouseEntries:
    return {
        house: dict(section.get("entries", {}))
        for house, section in sections.items()
    }


def _validate_flight_entry(entry: FlightEntry, context: str) -> None:
    missing_fields = [field for field in FLIGHT_ENTRY_FIELDS if field not in entry]
    if missing_fields:
        missing_fields_text = ", ".join(missing_fields)
        raise KeyError(f"Missing flight entry fields for {context}: {missing_fields_text}")


def _build_target_house_catalog(
    meta: Dict[int, TargetHouseMeta],
    entries: TargetHouseEntries,
) -> Dict[int, FlightCatalogSection]:
    missing_houses = [house for house in ALL_HOUSES if house not in meta]
    if missing_houses:
        raise KeyError(f"Missing target house definitions: {', '.join(map(str, missing_houses))}")

    catalog: Dict[int, FlightCatalogSection] = {}
    for house, section_meta in meta.items():
        missing_meta_fields = [field for field in FLIGHT_SECTION_META_FIELDS if field not in section_meta]
        if missing_meta_fields:
            missing_meta_text = ", ".join(missing_meta_fields)
            raise KeyError(f"Missing target house meta for house {house}: {missing_meta_text}")

        house_entries = dict(entries.get(house, {}))
        if house in BASE_ENTRY_REQUIRED_TARGET_HOUSES and not house_entries:
            raise KeyError(f"Missing base flight entries for target house {house}")

        for source_house, entry in house_entries.items():
            _validate_flight_entry(entry, f"base {source_house}R->{house}")

        catalog[house] = {
            "title": section_meta["title"],
            "theme": section_meta["theme"],
            "entries": house_entries,
        }
    return catalog


def _register_source_flight_overrides(
    catalog: Dict[int, FlightCatalogSection],
    source_overrides: SourceFlightOverrides,
) -> None:
    for source_house, target_overrides in source_overrides.items():
        for target_house, entry in target_overrides.items():
            section = catalog.get(target_house)
            if not section:
                raise KeyError(f"Unknown flight target house: {target_house}")

            _validate_flight_entry(entry, f"override {source_house}R->{target_house}")
            section.setdefault("entries", {})[source_house] = entry


TARGET_HOUSE_FLIGHT_META = _extract_target_house_meta(TARGET_HOUSE_FLIGHT_SECTIONS)
TARGET_HOUSE_FLIGHT_ENTRIES = _extract_target_house_entries(TARGET_HOUSE_FLIGHT_SECTIONS)

HOUSE_RULER_FLIGHT_CATALOG = _build_target_house_catalog(
    TARGET_HOUSE_FLIGHT_META,
    TARGET_HOUSE_FLIGHT_ENTRIES,
)
_register_source_flight_overrides(HOUSE_RULER_FLIGHT_CATALOG, SOURCE_FLIGHT_OVERRIDES)


def get_house_ruler_flight_entry(source_house: int, target_house: int) -> Optional[Dict[str, Any]]:
    section = HOUSE_RULER_FLIGHT_CATALOG.get(target_house)
    if not section:
        return None

    entry = section.get("entries", {}).get(source_house)
    if not entry:
        return None

    return {
        "target_house": target_house,
        "target_title": section["title"],
        "target_theme": section["theme"],
        "source_house": source_house,
        "summary": entry["summary"],
        "positive": entry["positive"],
        "negative": entry["negative"],
    }


