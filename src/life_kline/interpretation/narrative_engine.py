def _build_narrative(
    base_map: dict[str, str],
    label: str,
    sign: str,
    dignity: str = "peregrine",
    house_type: str = "succedent",
    has_hard_aspect: bool = False,
    has_harmonious_aspect: bool = False,
    has_chart_ruler_aspect: bool = False,
    reception_by: str = "",
) -> str:
    """通用叙事构建函数——任意行星×星座 + 四层修饰。"""
    base = base_map.get(sign, f"{label}在{sign}。")
    parts = [base]
    if dignity in DIGNITY_MODIFIER:
        parts.append(DIGNITY_MODIFIER[dignity].format(planet_label=label))
    if house_type in HOUSE_TYPE_MODIFIER:
        parts.append(HOUSE_TYPE_MODIFIER[house_type].format(planet_label=label))
    if has_hard_aspect:
        parts.append(ASPECT_HARD.format(planet_label=label))
    if has_harmonious_aspect:
        parts.append(ASPECT_HARMONIOUS.format(planet_label=label))
    if has_chart_ruler_aspect:
        parts.append(ASPECT_CHART_RULER.format(planet_label=label))
    if reception_by:
        from ..domains.helpers import plabel as _pl
        parts.append(RECEPTION_TEMPLATE.format(
            planet_label=label,
            receptor=_pl(reception_by),
            receptor_title=f"{_pl(reception_by)}在这个领域给你铺了路、搭了桥",
        ))
    return "\n\n".join(parts)


